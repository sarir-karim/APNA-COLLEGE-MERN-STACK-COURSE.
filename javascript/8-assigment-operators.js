// previously we have learned about the assigment operator but now we are going to study some more of it.
/**
 * age +=2; (age = age + 2);
 * age -=2; (age = age -2);
 * age *=2; (age = age *2);
 * age /=2; (age = age /2);
 * age %=2; (age = age %2);
 * */ 


let age = 2;
age += 2;  
console.log(age) // 4

age -= 2;
console.log(age) // 2

age /= 2;
console.log(age) //1

age *=2;
console.log(age) //2

age %=2;
console.log(age) //0

